
/**
 * This class is used to create State objects.
 * @author 140012021
 *
 */
public class States {

	String name;
	boolean isAccept;
	boolean isReject;
	boolean isIntial;
	
	public States(String name, boolean isAccept, boolean isReject, boolean isIntial){
		this.name = name;
		this.isAccept = isAccept;
		this.isReject = isReject;
		this.isIntial = isIntial;
	}
	
	
	
}
